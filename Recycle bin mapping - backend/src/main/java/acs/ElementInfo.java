package acs;

public class ElementInfo {
	private String elementId;
		
	public ElementInfo() {
		
	} 
	
	public ElementInfo(String elementId) {
		this.elementId = elementId;
	}
	
	public String getElementId() {
		return elementId;
	}

	public void setElementId(String elementId) {
		this.elementId = elementId;
	}
	
	
}